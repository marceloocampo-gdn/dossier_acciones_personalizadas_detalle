CONSUMO_CLIE AS (

    SELECT
        FFM.SOCI_SOCI_ID,
        DATE_TRUNC(MONTH, A.TIEM_DIA_ID) AS MES,
        SUM(A.VNTA_IMPORTE_SIN_IVA) AS CONSUMO_PRE,
        ROUND(SUM(A.VNTA_IMPORTE_SIN_IVA - (A.VNTA_UNIDADES * A.VNTA_COSTO_PROM_POND))) AS GB1,
        COUNT(DISTINCT(A.TICKET)) AS TICKETS_PRE,
        ROUND(CONSUMO_PRE / TICKETS_PRE) AS TICK_PROM_PRE,
        ROUND((GB1 / TICKETS_PRE)) AS GB1_PROM_PRE
    FROM
        MSTRDB.DWH.FT_FDLN_MOVIMIENTOS AS FFM
        INNER JOIN MSTRDB.DWH.LU_GEOG_LOCAL AS LGL ON FFM.GEOG_LOCL_ID = LGL.GEOG_LOCL_ID AND LGL.GEOG_UNNG_ID = 2
        INNER JOIN MSTRDB.DWH.FT_VENTAS AS A
            ON A.GEOG_LOCL_ID = FFM.GEOG_LOCL_ID
            AND A.VNTA_NRO_TICKET = FFM.VNTA_NRO_TICKET
            AND A.VNTA_NRO_CAJA = FFM.VNTA_NRO_CAJA
            AND A.TIEM_DIA_ID = FFM.TIEM_DIA_ID
            AND FFM.FDLN_MOVT_TIPO = 'RP'
    WHERE
        YEAR(A.TIEM_DIA_ID) >= YEAR(CURRENT_DATE) - 1
        AND FFM.SOCI_SOCI_ID IN (SELECT DISTINCT SOCI_SOCI_ID FROM VTA_GENERADA_CUPONES)
        AND (A.GEOG_LOCL_ID, A.VNTA_NRO_TICKET, A.VNTA_NRO_CAJA, A.TIEM_DIA_ID) NOT IN (SELECT DISTINCT GEOG_LOCL_ID, VNTA_NRO_TICKET, VNTA_NRO_CAJA, TIEM_DIA_ID FROM TICKETS_SEGMENTADOS)
        AND MONTH(A.TIEM_DIA_ID) NOT IN (3, 12)
    GROUP BY
        ALL
    ORDER BY
        1, 2 DESC

) --SELECT * FROM CONSUMO_CLIE;
,
INC AS (

    SELECT
        V.SOCI_SOCI_ID,
        V.MES,
        VTA_SIN_IVA_CUPONES,
        GB1_CUPONES,
        TICKETS_CUP,
        TICK_PROM_CUP,
        GB1_PROM_CUP,
        C.CONSUMO_PRE,
        C.GB1,
        C.TICKETS_PRE,
        C.TICK_PROM_PRE,
        C.GB1_PROM_PRE,
        ROW_NUMBER() OVER (PARTITION BY V.SOCI_SOCI_ID, V.MES ORDER BY C.MES DESC) AS R
    FROM
        VTA_GENERADA_CUPONES AS V
        LEFT JOIN CONSUMO_CLIE AS C ON V.SOCI_SOCI_ID = C.SOCI_SOCI_ID
    WHERE
        C.MES < V.MES
    QUALIFY
        R <= 3
    ORDER BY
        V.SOCI_SOCI_ID,
        C.MES DESC
    
) --SELECT * FROM INC;
,
INCREMENTAL AS (
    SELECT
        SOCI_SOCI_ID,
        MES,
        MAX(TICK_PROM_CUP)        AS TICK_PROM_CUPONES,
        MAX(GB1_PROM_CUP)         AS GB1_CUPONES,
        MAX(TICKETS_CUP)          AS TICKETS_CUPONES,
        ROUND(AVG(TICK_PROM_PRE)) AS TICK_PROM_PRE,
        ROUND(AVG(GB1_PROM_PRE))  AS GB1_PROM_PRE
    FROM INC
    GROUP BY SOCI_SOCI_ID, MES
),

INCREMENTAL_FINAL AS (
    SELECT
        *,
        TICK_PROM_CUPONES - TICK_PROM_PRE AS VTA_INCREMENTAL,
        GB1_CUPONES - GB1_PROM_PRE        AS GB1_INCREMENTAL
    FROM INCREMENTAL

) --SELECT * FROM INCREMENTAL_FINAL ORDER BY 1;
,
