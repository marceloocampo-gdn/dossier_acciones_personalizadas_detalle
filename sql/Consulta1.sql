WITH PROMOCIONES_ABANDONO AS (

    SELECT
        CAMP_PROM_ID,
        COALESCE(PAZOS_CODIGO_PROMO, VNTA_CODIGO_PROMOCION) AS VNTA_CODIGO_PROMOCION,
