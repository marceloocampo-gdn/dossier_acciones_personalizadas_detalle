-- 1. Clientes que usaron descuento de cumpleaños desde 2026 en adelante
CUMPLE AS (
    SELECT DISTINCT
        m.SOCI_SOCI_ID,                          -- ajustar nombre real del campo cliente
        v.TIEM_DIA_ID                          AS FECHA_CUMPLE_USO,
        v.TICKET
    FROM 
        MSTRDB.DWH.FT_VENTAS_DESCUENTOS_LINEAS v
        INNER JOIN MSTRDB.DWH.FT_FDLN_MOVIMIENTOS m ON v.vnta_nro_ticket = m.vnta_nro_ticket AND v.vnta_nro_caja = m.vnta_nro_caja and v.geog_locl_id = m.geog_locl_id and v.tiem_dia_id = m.tiem_dia_id AND m.fdln_movt_tipo = 'RP'
    WHERE 
        v.TIEM_DIA_ID >= '2026-01-01'
        AND (v.VNTA_COD_DESCUENTO IN (3387 , 5000000006) OR v.VNTA_CODIGO_PROMOCION IN (3387 , 5000000006))

) 
,
