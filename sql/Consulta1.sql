        ROUND(SUM(FV.VNTA_IMPORTE_SIN_IVA - (FV.VNTA_UNIDADES * FV.VNTA_COSTO_PROM_POND))) AS GB1_PROMO,
        ROUND(GB1_PROMO / VTA_PROMO, 2) AS MARGEN_PROMO,
        COUNT(DISTINCT(FFM.SOCI_SOCI_ID)) AS SOCIOS_CON_PROMO,
        IMPACTADOS,
        ROUND((SOCIOS_CON_PROMO / IMPACTADOS) * 100, 2) AS REDENCION
    FROM
        MSTRDB.DWH.FT_VENTAS AS FV
        INNER JOIN TICKETS_TOTAL AS TT
            ON FV.TIEM_DIA_ID = TT.TIEM_DIA_ID 
            AND FV.VNTA_NRO_TICKET = TT.VNTA_NRO_TICKET 
            AND FV.GEOG_LOCL_ID = TT.GEOG_LOCL_ID
            AND FV.VNTA_NRO_CAJA = TT.VNTA_NRO_CAJA
        INNER JOIN MSTRDB.DWH.FT_FDLN_MOVIMIENTOS AS FFM
            ON FV.TIEM_DIA_ID = FFM.TIEM_DIA_ID
