SELECT
    UNIDAD_NEGOCIO,
    OBJETIVO,
    ACCION,
    ANIO,
    MES,
    TICKETS,
    VTA_PROMO,
    GB1_PROMO,
    SOCIOS_CON_PROMO AS CLIENTES_USO,
    IMPACTADOS,
    REDENCION
FROM
    (
    (SELECT * FROM ACCIONES_SEGMENTADOS)
    UNION ALL
    (SELECT * FROM ACCIONES_FOCO_GENERAL)
    UNION ALL
    (SELECT * FROM ACCION_ABANDONO_TOTAL)
    UNION ALL
    (SELECT * FROM ACCIONES_CUMPLE)
    UNION ALL
    (SELECT * FROM ACCIONES_CUOTA)
    UNION ALL
    (SELECT * FROM ACCIONES_MP)
    UNION ALL
    (SELECT * FROM ACCIONES_APP)
    UNION ALL
    (SELECT * FROM ACCIONES_BAS)
    )
WHERE
    ANIO >= 2025
GROUP BY
    ALL
ORDER BY
    2,
    3,
    1;