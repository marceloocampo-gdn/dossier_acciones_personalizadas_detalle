        COUNT(DISTINCT(A.TICKET)) AS TICKETS_CUP,
        ROUND((VTA_SIN_IVA_CUPONES / TICKETS_CUP)) AS TICK_PROM_CUP,
        ROUND((GB1_CUPONES / TICKETS_CUP)) AS GB1_PROM_CUP
